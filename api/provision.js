  site: Record<string, unknown>;
  branding: Record<string, unknown>;
  seo: Record<string, unknown>;
  niche: Record<string, unknown>;
  navigation: Record<string, unknown>;
  social: Record<string, unknown>;
  features: Record<string, unknown>;
