// ── Helpers ──────────────────────────────────────────────────
async function pushFile(ghToken, org, repo, path, content, message) {
  const b64 = Buffer.from(content).toString('base64');
  const headers = {
    'Authorization': `token ${ghToken}`,
    'Content-Type': 'application/json',
    'Accept': 'application/vnd.github.v3+json'
  };
  // Get existing SHA if file exists
  let sha;
  try {
    const r = await fetch(`https://api.github.com/repos/${org}/${repo}/contents/${path}`, { headers });
    if (r.ok) { const d = await r.json(); sha = d.sha; }
  } catch {}
  const body = { message, content: b64, ...(sha ? { sha } : {}) };
  const r = await fetch(`https://api.github.com/repos/${org}/${repo}/contents/${path}`, {
    method: 'PUT', headers, body: JSON.stringify(body)
  });
  return r.ok;
}

