async function pushFile(ghToken, org, repo, path, content, message) {
  const b64 = Buffer.from(content).toString('base64');
  const headers = {
    'Authorization': `token ${ghToken}`,
    'Content-Type': 'application/json',
    'Accept': 'application/vnd.github.v3+json'
  };
  let sha;
  try {
    const r = await fetch(`https://api.github.com/repos/${org}/${repo}/contents/${path}`, { headers });
    if (r.ok) { const d = await r.json(); sha = d.sha; }
  } catch {}
  const body = { message, content: b64, ...(sha ? { sha } : {}) };
  const r = await fetch(`https://api.github.com/repos/${org}/${repo}/contents/${path}`, {
    method: 'PUT', headers, body: JSON.stringify(body)
  });
  return r.ok;
}

function generateThemeCss(primaryHex, accentHex) {
  const p = hexToHsl(primaryHex);
  const a = accentHex ? hexToHsl(accentHex) : p;
  const parts = p.split(' ');
  const darkP = `${parts[0]} ${parts[1]} ${Math.max(20, parseInt(parts[2]) - 15)}%`;
  return `/* AUTO-GENERATED by DYNASTY-LAUNCHER */\n:root {\n  --background: 0 0% 100%;\n  --foreground: 222 20% 12%;\n  --card: 0 0% 100%;\n  --card-foreground: 222 20% 12%;\n  --primary: ${darkP};\n  --primary-foreground: 210 40% 98%;\n  --secondary: ${a};\n  --secondary-foreground: 222 47% 11%;\n  --muted: ${a};\n  --muted-foreground: 215 16% 47%;\n  --accent: ${a};\n  --accent-foreground: 222 47% 11%;\n  --destructive: 0 84% 60%;\n  --destructive-foreground: 210 40% 98%;\n  --border: 214 32% 91%;\n  --input: 214 32% 91%;\n  --ring: ${darkP};\n  --radius: 0.5rem;\n}\n.dark {\n  --background: 222 47% 11%;\n  --foreground: 210 40% 98%;\n  --primary: ${p};\n  --primary-foreground: 222 47% 11%;\n  --secondary: 217 32% 17%;\n  --secondary-foreground: 210 40% 98%;\n  --accent: ${a};\n  --accent-foreground: 210 40% 98%;\n  --border: 217 32% 17%;\n  --input: 217 32% 17%;\n  --ring: ${p};\n}`;
}

function hexToHsl(hex) {
  if (!hex || hex.length < 6) return '210 70% 35%';
  hex = hex.replace('#', '');
  const r = parseInt(hex.slice(0, 2), 16) / 255;
  const g = parseInt(hex.slice(2, 4), 16) / 255;
  const b = parseInt(hex.slice(4, 6), 16) / 255;
  const max = Math.max(r, g, b), min = Math.min(r, g, b);
  let h, s, l = (max + min) / 2;
  if (max === min) { h = s = 0; }
  else {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r: h = ((g - b) / d + (g < b ? 6 : 0)) / 6; break;
      case g: h = ((b - r) / d + 2) / 6; break;
      case b: h = ((r - g) / d + 4) / 6; break;
    }
  }
  return `${Math.round(h * 360)} ${Math.round(s * 100)}% ${Math.round(l * 100)}%`;
}

// ── 20i Helper ─────────────────────────────────────────────────────────────
function get20iAuth(config) {
  const general = config?.infrastructure?.twentyi_general;
  const oauth   = config?.infrastructure?.twentyi_oauth;
  if (!general) return null;
  return `Bearer ${Buffer.from(general).toString('base64')}`;
}

async function twentyiReq(auth, method, path, body) {
  const r = await fetch(`https://api.20i.com${path}`, {
    method,
    headers: { 'Authorization': auth, 'Content-Type': 'application/json' },
    ...(body ? { body: JSON.stringify(body) } : {})
  });
  const text = await r.text();
  try { return { ok: r.ok, status: r.status, data: JSON.parse(text) }; }
  catch { return { ok: r.ok, status: r.status, data: text }; }
}

