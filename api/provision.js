// ── Neon Helper ─────────────────────────────────────────────────────────────
async function provisionNeonDb(neonApiKey, projectName, region = 'aws-us-east-2') {
  const r = await fetch('https://console.neon.tech/api/v2/projects', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${neonApiKey}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      project: {
        name: projectName,
        region_id: region,
        pg_version: 16
      }
    })
  });
  if (!r.ok) {
    const err = await r.text();
    throw new Error(`Neon API ${r.status}: ${err.slice(0, 120)}`);
  }
  const d = await r.json();
  const project = d.project;
  const conn = d.connection_uris?.[0]?.connection_uri || d.connection_uris?.[0];
  return {
    project_id: project.id,
    project_name: project.name,
    region: project.region_id,
    database_url: conn,
    pooled_url: conn?.replace('.neon.tech', '-pooler.neon.tech') || conn,
    dashboard: `https://console.neon.tech/app/projects/${project.id}`
  };
}

